package prob01;

public class Printer{

	public void println(int b) {
		System.out.println(b);
		
	}
	public void println(boolean b) {
		System.out.println(b);
		
	}
	
	public void println(double b) {
		System.out.println(b);
		
	}
	
	public void println(String b) {
		System.out.println(b);
		
	}
}
